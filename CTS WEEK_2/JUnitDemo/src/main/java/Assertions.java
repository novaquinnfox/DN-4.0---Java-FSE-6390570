public class Assertions {
    public int add(int a,int b){
        return a+b;
    }
    public boolean isGreat(int a,int b){
        return a>b;
    }
    public boolean isLess(int a,int b){
        return a<b;
    }
    public Object getNullObject(){
        return null;
    }
    public Object getNotNullObject(){
        return new Object();
    }
}
